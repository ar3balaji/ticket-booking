<h1>Hilfe</h1>
Hier eine kleine Erklärung zu allen möglichen Aktionen:<p>

<b>Liste der momentan zum Verkauf stehenden Filme</b><br>
Gibt eine Übersicht der Filme aus, danach dann eine Übersicht<br>
der Vorstellungen, die für einen Film noch offen sind und wieviele<br>
Online-Plätze für diese Vorstellung noch offen sind.<p>
<b>Wichtig</b>: Eine Vorstellung hat immer das Format:<p>
"<b>01.01.2001, 20:00</b>"

<p>
<b>Film in den Verkauf aufnehmen und Vorstellungen hinzufügen</b><br>
Wie der Name schon sagt, Gibt man den Filmnamen ein und legt dann<br>
eine erste Vorstellung fest, indem man das Datum, die Uhrzeit und die<br>
Anzahl der online zu verkaufenden Tickets eingibt.
<p>
<b>Vorstellung nachträglich zu einem Filmhinzufügen</b><br>
Und schon wieder ein selbstläufer. Man wählt einen Film aus und fügt<br>
diesem noch eine Vorstellung hinzu.
<p>
<b>Zeit/Datum einer Vorstellung ändern</b><br>
Film auswählen, Vorstellung auswählen und einfach überschreiben.
<p>
<b>Anzahl der Tickets zu einer Vorstellung ändern</B><br>
Ändert ggfs. die Anzahl der Online zur Verfügung stehenden Tickets.
<p>
<b>einzelne Vorstellung löschen</b><br>
Löscht eine Vorstellung zu einem Film.
<p>
<b>Film löschen</b><br>
Der Film wird ganz aus der Datenbank entfernt
<p>
<b>alle Abholnr. ansehen</b><br>
Gibt eine Liste sämtlicher vergebener Abholnummern aus.
<p>
<b>Abholnr. verwenden</b><br>
Man kann die Abholnr. eingeben, die Daten überprüfen, Karten ausgeben und <br>
die Daten zur Abholnr. dann löschen.<p>
